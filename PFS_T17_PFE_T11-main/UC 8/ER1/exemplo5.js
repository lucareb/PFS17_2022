numero = 5;

switch (numero) {
    case 1:
        console.log("O número selecionado foi o 1")
        break;
    case 2: 
        console.log("O número selecionado foi o 2")
    case 3:
        console.log("O número selecionado foi o 3")
    case 4:
        console.log("O número selecionado foi o 4")

    default:
        console.log("Não foi selecionado nenhum número")
        break;
}